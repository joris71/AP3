				<h3>Le Début</h3>
				<p><img src="../images/CSCN-768x351.png" alt="" class="img-rounded pull-right" width="300" > Et cette histoire commence en 1994. Année au cours de laquelle l’USAM décide de dissoudre sa troisième équipe seniors amateurs. Poussés vers la sortie, Samuel DUFES et d’autres « éjectés » de la Green Team refusent la fatalité. Tous veulent continuer le hand chez eux, à Nîmes. Dans la vie, Samuel est conducteur de trains. C’est, tout naturellement, qu’il contacte la section omnisports des cheminots nîmois. En quelques semaines, l’affaire est faite, c’est l’acte de naissance du CSC Nîmes Handball. Samuel DUFES en devient le premier président.</p>
				<h3>Le deuxième club du Gard</h3>
				<p>De fil en aiguille, le CSCN devient, dans la première moitié des années 2010, le deuxième club du Gard en nombre de licenciés : 294 en 2013. D’un point de vue sportif, l’équipe fanion des cheminots nîmois parvient à se hisser jusqu’à la Nationale 3, l’équivalent de la cinquième division. Mais l’’euphorie est de courte durée, le temps de deux saisons en N3. À partir de 2015, le club connait une période difficile : Beaucoup de départs et très peu d’arrivées. Un nouveau projet s’instaure en 2018. Il est freiné par la pandémie de Covid-19 et ses différents confinements. Le club doit prendre son mal en patience malgré l’arrivée de figures emblématiques : Grégoire DETREZ, entre autres, champion d’Europe avec les Bleus en 2010.</p>
				
